import 'package:flutter/material.dart';

class LagoonTextStyle {
  static TextStyle title13(Color color) {
    return TextStyle(
      fontSize: 13,
      fontWeight: FontWeight.w400,
      color: color,
    );
  }

  static TextStyle title12bold() {
    return const TextStyle(
      fontSize: 12,
      fontWeight: FontWeight.w500,
    );
  }

  static TextStyle title14(Color color) {
    return TextStyle(
      fontSize: 14,
      fontWeight: FontWeight.w400,
      color: color,
    );
  }

  static TextStyle title17bold() {
    return const TextStyle(
      fontSize: 17,
      fontWeight: FontWeight.w600,
    );
  }

  static TextStyle title14bold(Color color) {
    return TextStyle(
      fontSize: 14,
      fontWeight: FontWeight.w500,
      color: color,
    );
  }

  static TextStyle title10(Color color) {
    return TextStyle(
      fontSize: 10,
      fontWeight: FontWeight.w400,
      color: color,
    );
  }
}
